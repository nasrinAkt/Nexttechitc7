package for_loop;

public class class4 {

	public static void main(String[] args) {
		class4 boy = new class4();
		boy.paper();
		boy.pen();
		boy.pan();

	}
public void paper() {//print 0-9
for(int i =0; i<10; i++) {	
System.out.println(i);	

}
}
	
public void pen() {//print 0-24
for(int i =0; i<25; i++) {	
System.out.println(i);	

}
}	
public void pan() {//print 2-49
for(int i =2; i<50; i++) {	
System.out.println(i);	

}
}	
	
	
}
